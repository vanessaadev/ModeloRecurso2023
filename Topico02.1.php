                        <p class="SubTitulo">EDUCAÇÃO</p>
                        <hr>


                        <p>No que tange ao atendimento dos adolescentes em cumprimento de medidas socioeducativas pela setorial da Educação, a Câmara de Educação Básica do Conselho Nacional de Educação definiu, através da Resolução nº 03/2016, as diretrizes nacionais para o atendimento escolar de adolescentes e jovens em cumprimento de medidas socioeducativas.</p>

                        <p>A Resolução CEB e CNE nº 03/2016 está disponível no material complementar.</p>

                        <p>Abordaremos abaixo as diretrizes que consideramos mais relevantes. Antes disso é preciso destacar os princípios do atendimento escolar a esse público, abordados no Art. 4º da citada Resolução.</p>

                        <ul class="none">
                            <li>A prevalência da dimensão educativa sobre o regime disciplinar;</li>

                            <li>A escolarização como estratégia de reinserção social plena, articulada à reconstrução de projetos de vida e à garantia de direitos;</li>

                            <li>A progressão com qualidade, mediante o necessário investimento na ampliação de possibilidades educacionais;</li>

                            <li>O investimento em experiências de aprendizagem social e culturalmente relevantes, bem como do desenvolvimento progressivo de habilidades, saberes e competências;</li>

                            <li>O desenvolvimento de estratégias pedagógicas adequadas às necessidades de aprendizagem de adolescentes e jovens, em sintonia com o tipo de medida aplicada;</li>

                            <li>A prioridade de adolescentes e jovens em atendimento socioeducativo nas políticas educacionais;</li>

                            <li>O reconhecimento da singularidade e a valorização das identidades de adolescentes e jovens;</li>

                            <li>O reconhecimento das diferenças e o enfrentamento a toda forma de discriminação e violência, com especial atenção às dimensões sociais, geracionais, raciais, étnicas e de gênero.</li>
                        </ul>


                        <p>O documento define ainda, em seu Art. 5º, que os entes federados, em regime de colaboração, devem atuar para a oferta, a qualificação e a consolidação do atendimento escolar aos adolescentes e jovens em cumprimento de medida socioeducativa através algumas estratégias, como:</p>

                        <p>Clique aqui para conferir as estratégias do cumprimento da medida socioeducativa.</p>

                        <ul class="none">
                            <li>A inserção de ações voltadas para o atendimento escolar, no âmbito do SINASE, nos Planos Municipais, Estaduais e Distrital de Educação;</li><br>

                            <li>A implementação de políticas, programas, projetos e ações educacionais para a qualificação da oferta de escolarização, no âmbito do SINASE, contemplando as diferentes modalidades e etapas do atendimento socioeducativo;</li><br>

                            <li>A integração dos diferentes sistemas de informação para identificação da matrícula, acompanhamento da frequência e do rendimento escolar de adolescentes e jovens em atendimento socioeducativo;</li><br>

                            <li>O aperfeiçoamento e a adequação qualificada e contínua do censo escolar para atendimento às especificidades educacionais de adolescentes e jovens em cumprimento de medidas socioeducativas;</li><br>

                            <li>A promoção da participação de adolescentes e jovens em atendimento socioeducativo em exames de larga escala, nacionais e locais, em especial aqueles voltados à produção de indicadores educacionais, à certificação e ao acesso à Educação Superior;</li><br>

                            <li>A promoção de parcerias com instituições de Educação Superior para o desenvolvimento de ações de pesquisa e extensão que contribuam para a criação, implementação e fortalecimento de políticas públicas educacionais no âmbito do SINASE;</li><br>

                            <li>A implementação de políticas, programas, projetos e ações educacionais, por meio de parcerias com instituições públicas de Educação Profissional e Tecnológica, com os serviços nacionais de aprendizagem e outras entidades sociais para a inserção de adolescentes e jovens do sistema socioeducativo ou de seus egressos, como aprendizes e estagiários do Ensino Médio ou da Educação Superior, em órgãos da administração pública direta ou indireta e da iniciativa privada.</li><br>
                        </ul>

                        <p>Lei nº 10.097/2000 (Lei da Aprendizagem) disponível no material complementar.</p>

                        <p>A resolução define também que o atendimento escolar ao público em tela deve ser estruturado de modo intersetorial e cooperativo articulado às políticas públicas de assistência social, saúde, esporte, cultura, lazer, trabalho e justiça. Devendo para tanto (Art. 6º): Clique aqui para saber mais.</p>

                        Definir, no âmbito de sua administração, instância gestora responsável pela implementação e acompanhamento da escolarização de adolescentes e jovens em cumprimento de medidas socioeducativas e dos egressos;
                        Formalizar instrumentos para a cooperação técnica com outros órgãos setoriais para a efetivação de políticas no âmbito do SINASE;
                        Participar dos espaços políticos institucionais responsáveis pela definição das políticas e acompanhamento do SINASE;
                        Observar os parâmetros definidos pelos sistemas de ensino e pelo SINASE ligados ao campo educacional;
                        Manter interlocução constante entre a escola e os programas de atendimento socioeducativo;
                        Disponibilizar, a qualquer tempo e sempre que necessário, documentação escolar de adolescentes e jovens, em especial para subsidiar a definição da medida e a construção do Plano Individual de Atendimento;
                        Fortalecer a participação dos profissionais da educação na elaboração e acompanhamento do Plano Individual de Atendimento;
                        Articular organizações, serviços, programas e projetos disponíveis no território que potencializem e complementem as experiências educacionais em curso;
                        Manter compromisso com a garantia do sigilo, conservando dados referentes à situação do adolescente ou jovem em atendimento socioeducativo restritos àqueles profissionais a quem tal informação seja indispensável;
                        Articular o Plano Individual de Atendimento, com as ações desenvolvidas nas unidades escolares, com o projeto institucional e com o projeto político-pedagógico da unidade socioeducativa.
                        O Art. 7º trata ainda, que os sistemas de ensino devem assegurar a matrícula de estudante em cumprimento de medida socioeducativa sem qualquer discriminação, considerando que este direito é fundamental, público e subjetivo. Clique aqui para saber mais

                        Art. 7º [...]
                        § 1° A matrícula deve ser efetivada sempre que houver demanda e a qualquer tempo.
                        § 2° A matrícula deve ser assegurada independentemente da apresentação de documento de identificação pessoal, podendo ser realizada mediante a autodeclaração ou declaração do responsável.
                        § 3° Caso o estudante não disponha, no ato da matrícula, de boletim, histórico escolar, certificado, memorial ou qualquer outra documentação referente a sua trajetória escolar expedida por instituição de educação anterior, deverá ser realizada avaliação diagnóstica para definição da série ou ciclo, etapa e modalidade mais adequada ao seu nível de aprendizagem.
                        § 4° Para adolescentes e jovens já matriculados, logo após a definição da medida, deve ser feita articulação com a sua rede de ensino, com vistas à garantia da continuidade da escolarização em sua escola de origem ou escola de sua comunidade, sempre que não inviabilizado pela medida socioeducativa aplicada e respeitado o seu interesse.
                        § 5° Caso o estudante retorne a sua escola de origem, após cumprimento de internação provisória, a instituição de ensino deve viabilizar a recuperação do rendimento escolar, sem considerar as respectivas faltas no período.
                        § 6° Os adolescentes e jovens em cumprimento de medidas de Prestação de Serviços à Comunidade (PSC), Liberdade Assistida (LA) ou semiliberdade devem ter suas matrículas integradas às demais turmas de estudantes, não sendo permitida a formação de turmas exclusivas.
                        § 7° Nos casos de falta de qualquer tipo de documentação, seja de identificação pessoal ou escolar, os órgãos competentes pela sua expedição devem ser acionados pelos pais ou responsáveis, conselhos tutelares ou operadores de órgãos de assistência social ou de justiça.
                        § 8° Os sistemas de ensino devem, quando solicitado e a qualquer tempo, fornecer aos órgãos de assistência social e de justiça documentação relativa à trajetória escolar do estudante em cumprimento de medidas socioeducativas.







                        A Resolução, em seu Art. 14º, afirma que a escolarização desses adolescentes e jovens deve atentar para alguns atributos. Clique aqui para saber quais são:
                        Oferta de educação integral em tempo integral;
                        Oferta de Educação Profissional;
                        Garantia do Atendimento Educacional Especializado (AEE) aos estudantes com deficiência;
                        Acompanhamento pedagógico específico, garantido o sigilo;
                        Promoção de condições de acesso e permanência na Educação Superior;
                        Participação de adolescentes, jovens e suas famílias nos processos de gestão democrática da escola.


