                                <p>É importante destacar que o SINASE preconiza que as medidas socioeducativas devem ser aplicadas de forma progressiva, buscando alternativas menos restritivas sempre que possível, e aplicando a internação como último recurso, quando as outras medidas se mostrarem insuficientes para promover a responsabilização e ressocialização do adolescente. Além disso, o sistema estabelece que as medidas devam ser executadas em ambiente adequado, com respeito aos direitos humanos, garantindo a integridade física e moral dos adolescentes.</p>
                            </div>
                        </div>



                        <p>O ECA define ainda, que as medidas socioeducativas devem ser aplicadas de forma individualizada, levando em consideração as necessidades e características do adolescente, e devem ser orientadas pela pedagogia da proteção integral, visando ao seu desenvolvimento pessoal, social e educacional. O Estatuto preconiza a importância da promoção do respeito aos direitos humanos e da participação ativa do adolescente no processo socioeducativo. Além disso, o ECA estabelece que o cumprimento da medida socioeducativa deve ocorrer em meio aberto sempre que possível, priorizando o convívio familiar e comunitário.</p>

                        <p>O art. 1º, §2º da Lei nº 12594/2012 estabelece como principais objetivos das medidas socioeducativas:</p>

                        <ul class="none">
                            <li class="fadeInRight" data-wow-delay="0.3s"><i class="fa fa-bullseye CorVerdeClaroEstado"></i> A responsabilização do adolescente quanto às consequências lesivas do ato infracional, sempre que possível incentivando a sua reparação;</li>

                            <li class="fadeInRight" data-wow-delay="0.6s"><i class="fa fa-bullseye CorVerdeClaroEstado"></i> A integração social do adolescente e a garantia de seus direitos individuais e sociais, por meio do cumprimento de seus planos individuais de atendimento;</li>

                            <li class="fadeInRight" data-wow-delay="0.9s"><i class="fa fa-bullseye CorVerdeClaroEstado"></i> A desaprovação da conduta infracional, efetivando as disposições da sentença como parâmetro máximo de privação de liberdade ou restrição de direitos, observados os limites previstos em lei.</li>
                        </ul>


                        <p>Já o Art. 35º determina como princípios das medidas socioeducativas os que seguem:</p>

                        <ul class="none">
                            <li class="fadeInRight" data-wow-delay="1.3s"><i class="fa fa-bullseye CorVerdeClaroEstado"></i> Legalidade, não podendo o adolescente receber tratamento mais gravoso do que o conferido ao adulto;</li>

                            <li class="fadeInRight" data-wow-delay="1.6s"><i class="fa fa-bullseye CorVerdeClaroEstado"></i> Excepcionalidade da intervenção judicial e da imposição de medidas, favorecendo-se de meios de autocomposição de conflitos;</li>

                            <li class="fadeInRight" data-wow-delay="1.9s"><i class="fa fa-bullseye CorVerdeClaroEstado"></i> Prioridade das práticas ou medidas que sejam restaurativas e, sempre que possível, atendam às necessidades das vítimas;</li>

                            <li class="fadeInRight" data-wow-delay="2.3s"><i class="fa fa-bullseye CorVerdeClaroEstado"></i> Proporcionalidade em relação à ofensa cometida;</li>

                            <li class="fadeInRight" data-wow-delay="2.6s"><i class="fa fa-bullseye CorVerdeClaroEstado"></i> Brevidade da medida em resposta ao ato cometido, em especial o respeito ao que dispõe o art. 122 da Lei nº 8.069/1990;</li>

                            <li class="fadeInRight" data-wow-delay="2.9s"><i class="fa fa-bullseye CorVerdeClaroEstado"></i> Individualização, considerando-se a idade, capacidade e circunstâncias pessoais do adolescente;</li>

                            <li class="fadeInRight" data-wow-delay="3.3s"><i class="fa fa-bullseye CorVerdeClaroEstado"></i> Mínima intervenção, restrita ao necessário para a realização dos objetivos da medida;</li>

                            <li class="fadeInRight" data-wow-delay="3.6s"><i class="fa fa-bullseye CorVerdeClaroEstado"></i> Não discriminação do adolescente, notadamente em razão de etnia, gênero, nacionalidade, classe social, orientação religiosa, política ou sexual, ou associação ou pertencimento a qualquer minoria ou status;</li>

                            <li class="fadeInRight" data-wow-delay="3.9s"><i class="fa fa-bullseye CorVerdeClaroEstado"></i> Fortalecimento dos vínculos familiares e comunitários no processo socioeducativo.</li>
                        </ul>

