                            <p>Dentre os diversos direitos e prerrogativas, o ECA estabelece as medidas protetivas e as socioeducativas. As primeiras são abordadas no Art. 98, e devem ser aplicadas sempre que os direitos previstos no texto legal forem ameaçados ou violados por ação ou omissão da sociedade ou do Estado; por falta, omissão ou abuso dos pais ou responsável e em razão da sua própria conduta da criança ou do adolescente. Já as últimas, são aplicadas quando verificada a prática de ato infracional, que se trata da conduta descrita como crime ou contravenção penal (Art. 103).</p>

                            <p>Nesse contexto deve-se compreender, segundo o Art. 104 do mesmo dispositivo, que os menores de dezoito anos são penalmente inimputáveis, ou seja, quando do cometimento de ato infracional serão aplicados a eles as medidas previstas na Lei nº 8.069/1990 e não aquelas previstas no Código Penal. Cabe destacar que, nessa avaliação, deve-se considerar a idade na data do fato (Parágrafo Único – Art. 104º) e não da sentença.</p>
