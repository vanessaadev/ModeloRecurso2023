                        <p>No mesmo artigo, o ECA disciplina que poderão ser aplicadas quaisquer medidas protetivas elencadas nos incisos I a VI do art. 101 em conjunto com as socioeducativas, ou mesmo isoladas, quando o ato infracional for praticado por criança. Destaca também que a aplicação da medida considerará a capacidade do adolescente de cumpri-la, as circunstâncias e a gravidade do ato infracional.</p>

                        <!-- Importante -->
