Regimento Interno – RI
É um instrumento para a consolidação da autogestão do Serviço/Programa de Atendimento, sendo também requisito obrigatório para a inscrição do programa de atendimento no CMDCA.
Construído pela equipe do Programa de Atendimento e apresentado ao Conselho de Direitos, devendo conter no mínimo:
o detalhamento das atribuições e responsabilidades do dirigente, de seus prepostos, dos membros da equipe técnica e dos demais educadores;
a previsão das condições do exercício da disciplina, o respectivo procedimento de aplicação; 
a previsão da concessão de benefícios extraordinários e enaltecimento, tendo em vista tornar público o reconhecimento ao adolescente pelo esforço realizado na consecução dos objetivos do plano individual.  (Art. 11, III, BRASIL, 2012).








Termo de Parceria/Cooperação Técnica
É o instrumento onde se pactua o recebimento e o acompanhamento dos adolescentes para a Prestação de Serviço à Comunidade e os objetivos dos pactuantes de recíproca cooperação, com um fim de interesse comum, de atuação conjunta na execução da Medida Socioeducativa de Prestação de Serviço à Comunidade – PSC, com base na Lei nº 8.069/1990, Resolução CNAS nº 109/2009 e Lei nº 12.594/2012.
Como passos anteriores, a Celebração do Termo de Cooperação Técnica entre o(s) órgão(s) gestor(es) da Assistência Social e as entidades parceiras, recomenda-se:
Passos metodológicos:
Identificar, selecionar, cadastrar, e credenciar entidades (assistenciais, de saúde, de educação ou outros estabelecimentos congêneres, bem como programas governamentais ou não governamentais);
Realização de reunião de sensibilização ou mobilização da sociedade para a apresentação do Serviço/Programa;
Realizar visitas institucionais;
Orientar as entidades governamentais e não governamentais da rede de proteção, a solicitarem suas inscrições no Conselho Municipal dos Direitos da Criança e do Adolescente -CMDCA, para o recebimento e acompanhamento de jovens em cumprimento de Prestação de Serviço à Comunidade – PSC;
Especificar que as tarefas serão atribuídas conforme as aptidões do adolescente;
Reafirmar que a jornada máxima é de oito horas semanais, podendo ser aos sábados, domingos e feriados ou em dias úteis, de modo a não prejudicar a frequência à escola ou à jornada normal de trabalho (BRASIL, 1990).
O Art. 14 da Lei nº 12.594/2012, estabelece que cabe ao Programa municipal responsável pelos serviços de MSE, ou seja, as equipes do SUAS, realizar o credenciamento das instituições parceiras com a validação posterior do Sistema de Justiça.

Recomenda-se que sejam contempladas nesses termos algumas questões básicas, como:
Respeito ao adolescente e sua família;
Zelo por sua integridade física e psicológica nas dependências da instituição;
Não submeter o adolescente a atividades que caracterizem trabalho;
Não impor ao adolescente valores religiosos;
Relatar periodicamente a evolução do adolescente à equipe da Assistência Social;
Comprometer-se com a construção da tarefa a ser desempenhada pelo adolescente pelo adolescente, instituição parceira;
Compromissos da Assistência Social;
Coordenar o processo de construção metodológica da tarefa a ser cumprida junto com o adolescente/família e a pessoa de referência socioeducativa da instituição parceira;
Prestar apoio técnico e logístico (quando houver disponibilidade) à instituição parceira e ao adolescente para a execução da medida;
Monitorar, de preferência semanalmente, o desenvolvimento das atividades do adolescente junto à pessoa de referência socioeducativa da instituição parceira;
Avaliar o cumprimento da medida ao final do processo junto ao adolescente e à instituição parceira.
Além desses compromissos é importante incluir nesse documento:
disposições gerais que esclarecem o caráter gratuito das atividades realizadas pelo adolescente;
a liberdade de se desligar do credenciamento a qualquer momento;
a não criação de qualquer vínculo trabalhista ou pecuniário com a instituição parceira (o que não impede de fornecer alimentação e auxílio transporte ao adolescente).


Com relação às entidades que receberão os adolescentes para cumprimento da medida socioeducativa de prestação de serviços à comunidade e ao programa de atendimento, vale destacar que, o uso de castigo físico ou de tratamento cruel ou degradante, como formas de correção, disciplina, educação configuram-se como crime de violência contra a criança ou adolescente e, por isso, são vedados pelo ECA.

