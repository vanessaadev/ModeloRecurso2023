Projeto Político Pedagógico – PPP
O projeto pedagógico está previsto no SINASE e deve guardar consonância com os princípios deste, ser escrito de forma clara a partir de uma prática refletida, elaborada e construída através de recursos teóricos metodológicos, considerando condições e experiências concretas.
É um instrumento teórico metodológico a ser construído, reconstruído, socializado e implementado cotidianamente nas Unidades de Atendimento. Configura-se como o principal instrumento de identidade e validação do trabalho das unidades e/ou programas de atendimento socioeducativo.
Deve pautar-se em princípios norteadores que articulam as ações nas áreas da Educação, Saúde, Assistência Social, Justiça, Segurança, Cultura, Esporte e Lazer. Esses princípios devem aparecer, de forma articulada, como resultado da pactuação das diversas setoriais, a partir do diagnóstico que será o ponto de partida para o planejamento e elaboração da proposta pedagógica.








Além disso, deve ser pensado, discutido e elaborado pela equipe multidisciplinar/intersetorial e executado por todos os operadores das medidas. Deve abordar o espaço físico, infraestrutura, capacidade de atendimento da Unidade, recursos humanos ações/atividades, referencial teórico-metodológico, recursos financeiros, monitoramento e avaliação. 
Este projeto norteará a elaboração dos demais documentos institucionais, como o regimento interno e o PIA.
