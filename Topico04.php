                        O SERVIÇO DE PROTEÇÃO SOCIAL A ADOLESCENTES EM CUMPRIMENTO DE MEDIDA SOCIOEDUCATIVA DE LIBERDADE ASSISTIDA (LA) E DE PRESTAÇÃO DE SERVIÇOS À COMUNIDADE (PSC)
Tendo como base a Tipificação Nacional dos Serviços Socioassistenciais, trataremos a seguir das características desse Serviço e o direcionamento do acompanhamento aos adolescentes e jovens, em cumprimento de medida socioeducativa em meio aberto, e suas famílias.
A execução do Serviço deve prover: atenção psicossocial, acompanhamento, viabilização do acesso a direitos e serviço e a possibilidade de ressignificar valores que contribuem com a interrupção da trajetória infracional, através do acompanhamento com frequência mínima semanal e tendo como base a responsabilização. Conforme ilustrado abaixo:








A Tipificação detalha o Serviço especificando seus usuários, formas de acesso, unidade de oferta e sua abrangência e período de funcionamento, finalidade do Serviço, articulação em rede necessária, o impacto social esperado e seus objetivos. A fim de facilitar a compreensão e absorção, ilustramos abaixo as informações sintetizadas.












Ressaltamos o destaque da excepcionalidade de a idade máxima ser de 21 anos para cumprimento da medida socioeducativa, considerando que ela pode ser aplicada até, no máximo, 3 anos, e que a data do ato infracional é que configura a aplicação da medida socioeducativa. Assim, ao completar 21 anos, o jovem em cumprimento de qualquer medida socioeducativa deve ser automaticamente liberado.
Além disso, evidencio a existência dos CREAS Regionais no estado do Ceará em 3 regiões diversas com 17 municípios vinculados no total.










Aqui a Tipificação ressalta a necessidade de elaboração do Plano Individual de Atendimento (PIA) com a participação do adolescente e da sua família em concordância com o SINASE. Há destaque também, com relação ao acompanhamento contínuo que possibilite o desenvolvimento do PIA.

