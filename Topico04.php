Já o Caderno do SINASE, elaborado pelo CONANDA (2006), prevê que a equipe mínima do programa de atendimento as medidas socioeducativas em meio aberto, especificando-a por tipo de medida conforme abaixo:





	
No caso desta medida, a referência socioeducativa é o profissional de nível superior ou com função de gerência ou coordenação nos locais de prestação de serviço comunitário, que será responsável geral tanto pelos adolescentes prestadores de serviço comunitário quanto pelo funcionário guia.
É importante também a existência do guia socioeducativo, que é o profissional do local de prestação de serviço diretamente ligado ao exercício da atividade realizada pelos adolescentes.






Cabe destacar que o Caderno do SINASE (2006) ainda aborda a Liberdade Assistida Comunitária (LAC) e a Liberdade Assistida Institucional (LAI), todavia estas modalidades de Liberdade Assistida foram unificadas e tipificadas no Serviço aqui tratado que em 2009 foi aprovado e organizado por níveis de complexidade do SUAS.
O próprio Caderno do SINASE (2006) pontuava que os técnicos, contratados pela instituição executora da antiga LAI, é que faziam diretamente acompanhamento com os adolescentes, não existindo a figura do orientador comunitário.







Precisamos considerar que a relação educativa pressupõe o estabelecimento de vínculo, que por sua vez, depende do grau de vinculação com o adolescente. Dessa forma, é essencial que o profissional tenha tempo para prestar atenção no adolescente, e que ele tenha um grupo reduzido destes sob sua responsabilidade.












O trabalho social com famílias é diretriz metodológica para as duas proteções (básica e especial) do SUAS. Trata-se de conduta metodológica, que exige mudança de processos de trabalho e de posturas profissionais frente aos desafios postos pelas famílias que procuram atendimento nos equipamentos da Assistência Social.
No caso das famílias com adolescentes em cumprimento de medidas socioeducativas em meio aberto, a atenção dos técnicos dos CREAS deve ser redobrada para não se deixarem levar por avaliações precipitadas, que acabam por culpabilizar a família pelo ato cometido pelo adolescente.
É sempre pertinente lembrar que a responsabilização pelo ato infracional é coletiva, com dimensões individuais (do adolescente), coletivas (da família, da comunidade) e sociais (sociedade, Estado e suas instituições).
