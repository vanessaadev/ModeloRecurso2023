O acompanhamento através do próprio Serviço das medidas socioeducativas em meio aberto, que deve ser registrado no Prontuário SUAS (físico e eletrônico), que será utilizado para embasar a elaboração do PIA. Cabe destacar que a utilização do Prontuário SUAS não é restrita ao Serviço PAEFI, este deve ser utilizado para registrar as intervenções durante o acompanhamento realizado pelo Serviço das MSE em meio aberto.
 	Caso seja identificada demanda de PAEFI, a família deve ser inserida no Serviço sem prejuízo dos demais encaminhamentos. Devendo ser utilizado apenas um único Prontuário SUAS para o acompanhamento pelos dois Serviços.
Devem ser realizados encaminhamentos de acordo com as necessidades apresentadas pelo adolescente/jovem e sua família, com atenção a concordância destes.
Precisamos estar atentos ao acompanhamento com frequência mínima semanal, através das diversas ações e intervenções pertencentes ao trabalho social com famílias.
A articulação e contatos frequentes entre o técnico ou equipe de referência do Serviço das MSE em meio aberto, e a Referência Técnica e o Orientador Socioeducativo, são essenciais para a qualidade do acompanhamento da medida de prestação de serviço à comunidade com vistas ao êxito.



O Plano Individual de Atendimento – PIA é a ferramenta estratégica na articulação com a rede socioassistencial e setorial, que visa proporcionar, através dos encaminhamentos (monitorados), vivências diferentes e que fortaleçam as potencialidades do adolescente e de sua família, a fim de reduzir à possibilidade de reincidência e subsidiar a construção de novo projeto de vida.
Com relação aos prazos para entrega e homologação do PIA o Art. 53 e 56 da Lei nº 12.594/2012 preveem o que segue:












Evidenciamos que, conforme o Art. 53 da mesma norma legal, o PIA é de responsabilidade da equipe técnica do programa de atendimento e deve ser elaborado com a participação efetiva do adolescente e de sua família. 
Como observado, há duas formas de homologação do PIA. A primeira é a homologação hábil, dentro do prazo e a tácita, quando não há manifestação explícita e que passado o tempo expresso compreende-se que não há impugnação, ou seja, que foi homologado.



O local de prestação de serviço à comunidade e os encaminhamentos ofertados, devem atender a necessidade e desejo do adolescente e sua família. As decisões são coletivas e devem ter o adolescente como protagonista.
Os acordos devem ser pactuados durante a construção do PIA em linguagem acessível e clara. Deve-se aqui, mais uma vez, vislumbrar a perspectiva da responsabilização vinculada à proteção social.
Conhecer o território e a sua rede de proteção é fundamental para proporcionar qualidade ao acompanhamento e, consequentemente, opções de encaminhamento a fim de garantir direitos e o efetivo atendimento.





Essa promoção da autoconfiança e capacidade de reflexão se dá, também, através do processo de elaboração do PIA de forma coletiva, priorizando a participação do adolescente nessas decisões. Os encaminhamentos também se configuram como estratégias para o alcance desses atributos, sempre com a concordância do usuário.
A inclusão das práticas restaurativas através da mediação de conflitos e círculos de paz é uma excelente estratégia no alcance da responsabilização atrelada à proteção social no qual o ato infracional deixa de ser o centro das atenções e as consequências com vistas a sua reparação se tornam o cerne do acompanhamento.




Nesse objetivo não se pode perder de vista a necessidade de monitorar os encaminhamentos realizados, priorizando principalmente o retorno do adolescente à escola e a sua qualificação profissional com vistas à inserção no mercado de trabalho.
Faz-se aqui importante o respeito e incentivo à Lei da Aprendizagem a fim de proporcionar oportunidades para essa qualificação, inserção no mercado de trabalho e geração de renda.


Aqui se deve atentar para os encaminhamentos, principalmente, dentro da rede socioassistencial através do acesso aos Serviços de Convivência e Fortalecimento de Vínculos (SCFV) e, consequentemente, ao Serviço de Proteção e Atendimento Integral à Família (PAIF), ambos ofertados pelo CRAS. Precisamos considerar também a rede socioassistencial privada, bem como as iniciativas da sociedade civil organizada que atuam de forma complementar nesse fortalecimento.
No próximo módulo, trataremos detalhadamente sobre a relação do Serviço de proteção social a adolescentes em cumprimento de medida socioeducativa de Liberdade Assistida (LA) e de Prestação de Serviços à Comunidade (PSC) com os demais Serviços do SUAS.
A Tipificação elenca ainda as seguranças que devem ser afiançadas durante a execução do Serviço das MSE em Meio Aberto.











As seguranças a serem afiançadas devem garantir também que os estereótipos, socialmente disseminados, não interfiram na acolhida do adolescente e de sua família. Esse processo pode ser realizado na Unidade do CREAS, em domicílio, no local da prestação de serviços ou mesmo em qualquer instituição pública. Aqui, percebemos a necessidade de qualificação de toda a rede de proteção para que o adolescente não seja violentado ou mesmo revitimizado.
É preciso ressaltar ainda a importância da Comissão Intersetorial no alcance dessas seguranças, bem como a necessidade de garantir a participação e protagonismo do adolescente na elaboração do seu PIA.
Com relação ao ambiente físico, não há uma normativa ou orientação específica para o Serviço da MSE em meio aberto na Política de Assistência Social. A Tipificação e o Caderno de Orientações Técnicas do CREAS determinam estrutura mínima da Unidade CREAS, onde o Serviço é ofertado. O caderno do SINASE traz orientações que coadunam com as previstas para os CREAS. Destacamos a necessidade de adaptação para atendimento de adolescentes com deficiência, sem nenhuma barreira física ou atitudinal. A citada orientação prevê:





























