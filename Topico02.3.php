                        ASSISTÊNCIA SOCIAL
O SUAS também integra o SINASE, e através da Resolução nº 109/2009 que aprova a Tipificação Nacional de Serviços Socioassistenciais, regulamentou o Serviço de Proteção Social a Adolescentes em cumprimento de Medida Socioeducativa de Liberdade Assistida (LA) e de Prestação de Serviços à Comunidade (PSC), que tem por finalidade prover atenção socioassistencial e acompanhamento a adolescentes e jovens em cumprimento de medidas socioeducativas em meio aberto, determinadas judicialmente. 
Este Serviço faz parte da Proteção Social Especial de Média Complexidade e é ofertado pelos CREAS sejam eles municipais ou regionais. 



O SUAS prevê uma rede socioassistencial através da oferta integrada de serviços, programas e benefícios. A materialização dessa rede se dá através das Unidades Socioassistenciais, como: CRAS, CREAS, Centro POP, Centro Dia e Acolhimentos e de forma complementar através da sociedade civil organizada. A atuação do SUAS deve ter como eixos norteadores a matricialidade sociofamiliar e o território.
Considerando a temática central dessa formação, abordaremos a Unidade CREAS especificamente e mais detalhadamente o Serviço de Proteção Social a Adolescentes em Cumprimento de Medidas Socioeducativas de Liberdade Assistida (LA) e de Prestação de Serviços à Comunidade (PSC).
Segundo a NOB RH Anotada e Comentada (2011), o Centro de Referência Especializado de Assistência Social – CREAS, é unidade pública de gestão estatal, ou seja, somente o Poder Público pode implementar essa Unidade. O CREAS configura-se como polo de referência, coordenador e articulador da proteção social especial de média complexidade.
