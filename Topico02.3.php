Vale ressaltar que é atribuição dos municípios, conforme ilustrado abaixo, criar e manter programa de atendimento para a execução das medidas socioeducativas em meio aberto (Art. 5º - Lei nº 12.594/2012).



Além disso, aqueles municípios que não possuem CREAS, devem dispor de profissionais para atender as demandas da média complexidade, como as medidas socioeducativas, conforme prevê o inciso VII do art. Protocolo de Gestão Integrada de Serviços, Benefícios e Transferências de Renda no âmbito do Sistema Único de Assistência Social - SUAS.

Sugerimos a leitura do Protocolo de Gestão Integrada de Serviços, Benefícios e Transferências de Renda no âmbito do Sistema Único de Assistência Social – SUAS e Nota Técnica nº 002/2022 – CAOPIJ – MPCE disponível no material complementar.

Para fins didáticos, utilizaremos a seguir, a nomenclatura Serviço das MSE em Meio Aberto como sinônimo do Serviço de Proteção Social a Adolescentes em Cumprimento de Medidas Socioeducativas de Liberdade Assistida (LA) e de Prestação de Serviços à Comunidade (PSC).
São dimensões do Serviço de MSE em Meio Aberto na Política de Assistência Social:
Complementaridade necessária entre o Serviço de MSE em Meio Aberto e os outros serviços do SUAS;
A Vigilância Socioassistencial como suporte à participação da Assistência Social no Sistema Socioeducativo; 
A Centralidade da intersetorialidade em todas as instâncias de planejamento e de execução do atendimento socioeducativo (operacionalidade se realiza na constituição das comissões intersetoriais de acompanhamento do sistema socioeducativo e dos planos de atendimento socioeducativo).

Percebe-se que tais dimensões dialogam com os princípios e objetivos do SINASE. 
Antes de iniciarmos a apresentação do Serviço, é preciso enfatizar a importância e centralidade da relação do órgão gestor da Assistência Social, com o Sistema de Justiça e a relação destes com o SINASE, tendo como base a intersetorialidade e interinstitucionalidade.
O atendimento socioeducativo extrapola as competências de um único segmento institucional. Dessa forma, as relações INTERINSTITUCIONAIS no Sistema de Garantia de Direitos - SGD são essenciais para garantir o atendimento voltado à responsabilização e a devida proteção integral dos adolescentes em cumprimento de medida socioeducativa. Em especial com atores diretamente envolvidos com o processo judicial, como: juízes, promotores e defensores.
Assim, É COMPETÊNCIA DO ÓRGÃO GESTOR MUNICIPAL, através de diálogo direto com esses atores, ESTABELECER FLUXOS E PROTOCOLOS QUE OFICIALIZEM a relação do atendimento do Serviço de MSE em Meio Aberto com o Sistema de Justiça, contemplando desde a aplicação até a execução da mesma.






	














O Serviço de proteção social a adolescentes em cumprimento de medida socioeducativa de Liberdade Assistida (LA), e de Prestação de Serviços à Comunidade (PSC), deve ser referência para o Sistema de Justiça encaminhar os adolescentes que cumprirão medidas socioeducativas em meio aberto.
 No atendimento o Serviço deve buscar a RESPONSABILIZAÇÃO e a PROTEÇÃO SOCIAL na seguinte vertente: Para saber mais sobre a Responsabilização e a proteção social, clique aqui 
A RESPONSABILIZAÇÃO: decorre da desaprovação da conduta infracional, por meio da aplicação da medida socioeducativa, que objetiva levar o adolescente à reflexão e ao reconhecimento de sua responsabilidade frente ao ato infracional cometido e suas consequências, o que vai ao encontro da ideia de responsabilidade individual. Apesar da responsabilização, o cumprimento de medidas socioeducativas deve necessariamente garantir os direitos individuais e sociais do adolescente, por meio de um atendimento que esteja atento às singularidades e potencialidades de cada um.
A PROTEÇÃO SOCIAL: É um mecanismo que busca a criação de condições favoráveis à superação da negação de direitos inerentes à trajetória da maioria desses adolescentes.







Antes de apresentar detalhadamente o Serviço de Proteção Social a adolescentes em cumprimento de medida socioeducativa de Liberdade Assistida (LA) e de Prestação de Serviços à Comunidade (PSC), apresentaremos esses dois tipos de medida visando alinhar os entendimentos e romper com alguns equívocos acerca da sua interpretação.


